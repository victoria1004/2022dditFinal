package kr.or.ddit.feed.vo;

/**
 * 투표 
 * @author PC-12
 */
public class VoteVO {
	private String vtNo; //투표번호
	private String optnNo; //선택지번호
	private String vtItm; //투표항목

	public String getVtNo() {
		return vtNo;
	}

	public void setVtNo(String vtNo) {
		this.vtNo = vtNo;
	}

	public String getOptnNo() {
		return optnNo;
	}

	public void setOptnNo(String optnNo) {
		this.optnNo = optnNo;
	}

	public String getVtItm() {
		return vtItm;
	}

	public void setVtItm(String vtItm) {
		this.vtItm = vtItm;
	}

	@Override
	public String toString() {
		return "VoteVO [vtNo=" + vtNo + ", optnNo=" + optnNo + ", vtItm=" + vtItm + "]";
	}
	
}
