package kr.or.ddit.admin.vo;

import java.io.Serializable;

public class LogVO implements Serializable{

	private String logNo;
	private String mbrId;
	private String logNm;
	private String logAddr;
	private String logIpAddr;
	private String logDt;
	
	private int rnum;
	
	public String getLogNo() {
		return logNo;
	}
	public void setLogNo(String logNo) {
		this.logNo = logNo;
	}
	public String getMbrId() {
		return mbrId;
	}
	public void setMbrId(String mbrId) {
		this.mbrId = mbrId;
	}
	public String getLogNm() {
		return logNm;
	}
	public void setLogNm(String logNm) {
		this.logNm = logNm;
	}
	public String getLogAddr() {
		return logAddr;
	}
	public void setLogAddr(String logAddr) {
		this.logAddr = logAddr;
	}
	public String getLogIpAddr() {
		return logIpAddr;
	}
	public void setLogIpAddr(String logIpAddr) {
		this.logIpAddr = logIpAddr;
	}
	public String getLogDt() {
		return logDt;
	}
	public void setLogDt(String logDt) {
		this.logDt = logDt;
	}
	
	public int getRnum() {
		return rnum;
	}
	public void setRnum(int rnum) {
		this.rnum = rnum;
	}
	@Override
	public String toString() {
		return "LogVO [logNo=" + logNo + ", mbrId=" + mbrId + ", logNm=" + logNm + ", logAddr=" + logAddr
				+ ", logIpAddr=" + logIpAddr + ", logDt=" + logDt + "]";
	}
	
	
}
