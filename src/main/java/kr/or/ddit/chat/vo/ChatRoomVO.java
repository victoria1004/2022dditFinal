package kr.or.ddit.chat.vo;

import java.util.List;

public class ChatRoomVO {
	private String chatrmNo;
	private String mbrId;
	private String chatrmNm;
	private String chatrmTm;
	private List<ChatVO> chatList;
	
	public String getChatrmNo() {
		return chatrmNo;
	}
	public void setChatrmNo(String chatrmNo) {
		this.chatrmNo = chatrmNo;
	}
	public String getMbrId() {
		return mbrId;
	}
	public void setMbrId(String mbrId) {
		this.mbrId = mbrId;
	}
	public String getChatrmNm() {
		return chatrmNm;
	}
	public void setChatrmNm(String chatrmNm) {
		this.chatrmNm = chatrmNm;
	}
	
	public String getChatrmTm() {
		return chatrmTm;
	}
	public void setChatrmTm(String chatrmTm) {
		this.chatrmTm = chatrmTm;
	}
	public List<ChatVO> getChatList() {
		return chatList;
	}
	public void setChatList(List<ChatVO> chatList) {
		this.chatList = chatList;
	}
	@Override
	public String toString() {
		return "ChatRoomVO [chatrmNo=" + chatrmNo + ", mbrId=" + mbrId + ", chatrmNm=" + chatrmNm + "]";
	}
	
	
	
}
